    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        UsuarioDAO dao = new UsuarioDAO();
        Usuario u = new Usuario();

        if (!txtNombreUsuario.getText().equals("") && !txtPassword.getText().equals("")) {
            
            u.setNombreUsuario(txtNombreUsuario.getText());
            u.setPassword(txtPassword.getText());
            u.setTipoUsuario(cbTipoUsuario.getSelectedItem().toString());
            
            if (dao.login(u)) {
                this.dispose();
                
                String idUsuario = String.valueOf(u.getIdUsuario());
                String nombreUsuario = u.getNombreUsuario();
                String tipoUsuario = u.getTipoUsuario();
                
                frmPaginaPrincipal frPaginaPrincipal = new frmPaginaPrincipal(idUsuario, nombreUsuario, tipoUsuario);
                frPaginaPrincipal.setVisible(true); //Que sea visible
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar sus datos");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

